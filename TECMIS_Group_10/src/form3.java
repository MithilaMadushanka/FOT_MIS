    private void add_attendanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_attendanceActionPerformed
        // TODO add your handling code here:
        att_message.setText(null);
        Object sess=com_session.getSelectedItem();
        Object type=com_type.getSelectedItem();
        Object status=com_status.getSelectedItem();
        String session_no=sess.toString();
        String att_type=type.toString();
        String att_status=status.toString();
        String reg_no=att_reg_no.getText();
        String subject_code=att_subject_code.getText();
        
        if(reg_no.equals("")||subject_code.equals("")||session_no.equals("")||att_type.equals("")||att_status.equals(""))
            att_message.setText("You should have to fill each of fields!..");
        else
        {
            Attendance att=new Attendance();
            att.setReg_no(reg_no);
            att.setSubject_code(subject_code);
            att.setSession_no(session_no);
            att.setType(att_type);
            att.setStatus(att_status);
            att.addAttendance();
            att_reg_no.setText(null);
            att_subject_code.setText(null);
        }    
    }//GEN-LAST:event_add_attendanceActionPerformed

