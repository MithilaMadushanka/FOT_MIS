    private void jLabel23MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel23MouseClicked
        // TODO add your handling code here:
        Student stu=new Student();
        String reg=stu.getReg();
        String sub_code=txtFind_sub_code.getText();
        if(sub_code.equals(""))
            message.setText("You have to mention Subject Code!..");
        else
        {
            message.setText(null);
            Connection con=MyConnection.getConnection();
            try {
                String sql="SELECT *FROM attendance WHERE reg_no='"+reg+"' AND subject_code='"+sub_code+"'";
                PreparedStatement ps=con.prepareStatement(sql);
                ResultSet rs=ps.executeQuery();
                attendance.setModel(DbUtils.resultSetToTableModel(rs));
            } catch (Exception e) {
            }
            txtreg_v.setText("xxxxxxxxxx");
            txtsub_v.setText("xxxxxxxxxx");
            txtover_v.setText("xxxxxxxxxx");
            txttheory_v.setText("xxxxxxxxxx");
            txtpractical_v.setText("xxxxxxxxxx");
            txtca_v.setText("xxxxxxxxxx");
        }

    }//GEN-LAST:event_jLabel23MouseClicked

