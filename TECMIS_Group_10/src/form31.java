    private void attendanceMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_attendanceMouseClicked
        // TODO add your handling code here:
        Attendance att=new Attendance();
        int r=attendance.getSelectedRow();
        String reg=attendance.getValueAt(r, 0).toString();
        String sub_code=attendance.getValueAt(r, 1).toString();
        String session=attendance.getValueAt(r, 2).toString();
        String type=attendance.getValueAt(r, 3).toString();
        String status=attendance.getValueAt(r, 4).toString();
       
        txtreg_v.setText(reg);
        txtsub_v.setText(sub_code);
        txtover_v.setText(att.getEligibility(reg, sub_code));
        txttheory_v.setText(att.getEligibilityTheory(reg, sub_code));
        txtpractical_v.setText(att.getEligibilityPractical(reg, sub_code));
        if(sub_code.equals("ICT2162")){
             txtpractical_v.setText("Theory Only");
             txtca_v.setText(att.getCAEligibility2(reg,sub_code));
             //att.addCA(reg,sub_code,att.getCAEligibility2(reg,sub_code));
        }     
        else{
            txtca_v.setText(att.getCAEligibility(reg,sub_code));
            //att.addCA(reg, sub_code, att.getCAEligibility(reg, sub_code));
        }    
    }//GEN-LAST:event_attendanceMouseClicked

