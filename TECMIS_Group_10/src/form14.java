    private void add_notification1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_notification1ActionPerformed
        Notification not=new Notification();
        String noti_id=txt_Noti_id_search.getText();
        if(noti_id.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please enter Notification Id!..");
        }
        else{
            ResultSet rs=not.searchNotification(noti_id);
            try{
                if(rs.next())
                {
                    txt_Noti_id1.setText(rs.getString("notification_id"));
                    txt_noti_date1.setText(rs.getString("date"));
                    txt_notification1.setText(rs.getString("notification"));
                    txt_noti_pub1.setText(rs.getString("publisher"));
                }
                else{
                    JOptionPane.showMessageDialog(null, "Notification not found Check your Notification Id");
                }
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, e);
            }    
        }
    }//GEN-LAST:event_add_notification1ActionPerformed

