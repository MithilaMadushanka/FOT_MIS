    void showdetails()
    {
        Connection con=MyConnection.getConnection();
        try {
            String sql="select reg_no,user_type,full_name,email,contact_no,dept from user where user_type='Student'";
            PreparedStatement ps=con.prepareStatement(sql);
            ResultSet rs=ps.executeQuery();
            stu_details.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
        }
    }        
