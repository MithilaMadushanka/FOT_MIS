    void showGrade()
    {
        Student stu=new Student();
        Marks mar=new Marks();
        
        Connection con=MyConnection.getConnection();
        try {
            String sql="call getGrade('"+stu.getReg()+"')";
            PreparedStatement ps=con.prepareCall(sql);
            ResultSet rs=ps.executeQuery();
            grade.setModel(DbUtils.resultSetToTableModel(rs));
            txtreg_v.setText(stu.getReg());
            txtsgpa.setText(mar.getSGPA(stu.getReg()));
            txtcgpa.setText(mar.getCGPA(stu.getReg()));
        } catch (Exception e) {
        }
    }        
