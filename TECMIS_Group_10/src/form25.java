    private void medi_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_medi_tableMouseClicked
        // TODO add your handling code here:
        int r=medi_table.getSelectedRow();
        String reg_no=medi_table.getValueAt(r, 0).toString();
        String course=medi_table.getValueAt(r, 1).toString();
        String type=medi_table.getValueAt(r, 2).toString();
        String session=medi_table.getValueAt(r, 3).toString();
        
        medi_reg.setText(reg_no);
        medi_sub.setText(course);
        medi_type.setText(type);
        medi_session.setText(session);
        
    }//GEN-LAST:event_medi_tableMouseClicked
    
    void showMedicals()
    {
        Connection con=MyConnection.getConnection();
        Student stu=new Student();
        try {
            String sql="SELECT *FROM medical WHERE reg_no='"+stu.getReg()+"'";
            PreparedStatement ps=con.prepareStatement(sql);
            ResultSet rs=ps.executeQuery();
            medi_table.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
        }
    } 
