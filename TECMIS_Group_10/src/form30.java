    private void jLabel17MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel17MouseClicked
        // TODO add your handling code here:
        Attendance att=new Attendance();
        String reg=txtFind_reg_no1.getText();
        String sub_code=txtFind_subject_code.getText();
        boolean flag=true;
        
        if(reg.equals("")||sub_code.equals(""))
            message2.setText("You have to mention Reg No and Subject Code!");
        else
        {
            txtreg1.setText(reg);
            txtsub1.setText(sub_code);
            if(sub_code.equals("ICT2162")){
                 message2.setText(null);
                 txteligibility.setText(att.getEligibilityTheory(reg, sub_code));
                 if(att.getEligibilityTheory(reg, sub_code).equals("Eligible"))
                 {
                     showt.setVisible(flag);
                     thappy.setVisible(flag);
                 }
                 else
                 {
                     showt.setVisible(flag);
                     tangree.setVisible(flag);
                 }    
            }     
            else{
                message2.setText(null);
                txteligibility.setText(att.getEligibility(reg, sub_code));
                if(att.getEligibilityPractical(reg, sub_code).equals("Eligible"))
                {
                    showt.setVisible(flag);
                    thappy.setVisible(flag);
                    showp.setVisible(flag);
                    phappy.setVisible(flag);
                } 
                else{
                    showt.setVisible(flag);
                    tangree.setVisible(flag);
                    showp.setVisible(flag);
                    pangree.setVisible(flag);
                }
            }    
            
            
        }    
    }//GEN-LAST:event_jLabel17MouseClicked

